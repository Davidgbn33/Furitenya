                <h5>A propos du jeu</h5>

                <p>Le mah-jong est un jeu de société d’origine chinoise qui se joue à quatre joueurs, avec des pièces appelées tuiles. Associant tactique, stratégie, calcul et psychologie, ainsi qu’une part plus ou moins importante de chance selon la règle jouée, le mah-jong est un jeu d’argent très populaire en Asie orientale et dans la plupart des communautés chinoises et japonaises du monde.</p>

                <p>Le thème du jeu s’apparente au rami par certains aspects et à certaines variétés de poker par d’autres, toutefois le mah-jong est beaucoup plus riche dans les possibilités qu’il offre par rapport à ces deux familles de jeux : chaque joueur, à son tour, prend une tuile, l’incorpore à sa main puis choisit une tuile de sa main à écarter. Identique dans presque toutes les règles pratiquées, le but du jeu est de rassembler des tuiles identiques ou consécutives trois par trois, ainsi qu’une paire.</p>

                <p>La composition et le déroulement du jeu varient peu, mais les règles, transmises principalement à l’oral, sont très variables d’une ville, d’une région et d’un pays à l’autre en Asie. En Occident, sa pratique a connu des modes, mais est restée privée jusqu’au début du XXIe siècle. En parallèle du jeu d’argent, une pratique de compétition s’est développée, d’abord au Japon, plus récemment en Chine et en Europe, avec des règles standard.</p>

